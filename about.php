			<h3>Tentang jogja</h3>
			<p>Nama Yogyakarta terambil dari dua kata, yaitu Ayogya atau Ayodhya yang berarti "kedamaian" (atau tanpa perang, a "tidak", yogya merujuk pada yodya atau yudha, yang berarti "perang"), dan Karta yang berarti "baik". Ayodhya merupakan kota yang bersejarah di India di mana wiracarita Ramayana terjadi. Tapak keraton Yogyakarta sendiri menurut babad (misalnya Babad Giyanti) dan leluri (riwayat oral) telah berupa sebuah dalem yang bernama Dalem Gerjiwati; lalu dinamakan ulang oleh Sunan Pakubuwana II sebagai Dalem Ayogya.</p>
			<h3>Pusaka dan Identitas Daerah</h3>
			<p>Tombak Kyai Wijoyo Mukti</p>
			<p>Merupakan Pusaka Pemberian Raja Kraton Yogyakarta Sri Sultan Hamengku Buwono X. Tombak ini dibuat pada tahun 1921 semasa pemerintahan Sri Sultan Hamengku Buwono VIII. Senjata yang sering dipergunakan para prajurit ini mempunyai panjang 3 meter. Tombak dengan pamor wos wutah wengkon dengan dhapur kudhuping gambir ini, landeannya sepanjang 2,5 meter terbuat dari kayu walikun, yakni jenis kayu yang sudah lazim digunakan untuk gagang tombak dan sudah teruji kekerasan dan keliatannya.</p>
			<p>Sebelumnya tombak ini disimpan di bangsal Pracimosono dan sebelum diserahkan terlebih dahulu dijamasi oleh KRT. Hastono Negoro, di dalem Yudonegaran. Pemberian nama Wijoyo Mukti baru dilakukan bebarapa hari menjelang upacara penyerahan ke Pemkot Yogyakarta, pada peringatan hari ulang tahun ke-53 Pemerintah kota Yogyakarta tanggal 7 Juni 2000. Upacara penyerahan dilakukan di halaman Balaikota dan pusaka ini dikawal khusus oleh prajurit Kraton ”Bregodo Prajurit Mantrijero”.</p>
			<p>Tombak Kyai Wijoyo Mukti melambangkan kondisi Wijoyo Wijayanti. Artinya, kemenangan sejati pada masa depan, di mana seluruh lapisan masyarakat dapat merasakan kesenangan lahir bathin karena tercapainya tingkat kesejahteraan yang benar-benar merata.</p>
			<h3>Geografi</h3>
			<p>Lokasi Kota Yogyakarta di Daerah Istimewa Yogyakarta. Kota Yogyakarta terletak di lembah tiga sungai, yaitu Sungai Winongo, Sungai Code (yang membelah kota dan kebudayaan menjadi dua), dan Sungai Gajahwong. Kota ini terletak pada jarak 600 KM dari Jakarta, 116 KM dari Semarang, dan 65 KM dari Surakarta, pada jalur persimpangan Bandung – Semarang – Surabaya – Pacitan. Kota ini memiliki ketinggian sekitar 112 m dpl.</p>
			<p>Meski terletak di lembah, kota ini jarang mengalami banjir karena sistem drainase yang tertata rapi yang dibangun oleh pemerintah kolonial, ditambah dengan giatnya penambahan saluran air yang dikerjakan oleh Pemkot Yogyakarta.</p>
			<h3>Batas wilayah</h3>
			<p>Kota Yogyakarta telah terintegrasi dengan sejumlah kawasan di sekitarnya, sehingga batas-batas administrasi sudah tidak terlalu menonjol. Untuk menjaga keberlangsungan pengembangan kawasan ini, dibentuklah sekretariat bersama Kartamantul (Yogyakarta, Sleman, dan Bantul) yang mengurusi semua hal yang berkaitan dengan kawasan aglomerasi Yogyakarta dan daerah-daerah penyangga (Depok, Mlati, Gamping, Kasihan, Sewon, dan Banguntapan).</p>
